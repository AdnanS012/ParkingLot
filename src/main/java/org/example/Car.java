package org.example;

public class Car {
    private final String color;
    private final String numberPlate;

    public Car(String color,String numberPlate) {
        this.color = color;
        this.numberPlate = numberPlate;;
    }

    public String getColor() {
        return color;
    }
    public String getNumberPlate() {
        return numberPlate;
    }



}
